    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Boton de guardar Arreglos
        
        if(jTextField1.getText().isEmpty()){
        JOptionPane.showMessageDialog(this, "Falta el nombre del arreglo","Advertencia",JOptionPane.WARNING_MESSAGE);
        jTextField1.requestFocus();
        }
        else if(jTextField2.getText().isEmpty()){
        JOptionPane.showMessageDialog(this, "Falta el precio del arreglo","Advertencia",JOptionPane.WARNING_MESSAGE);
        jTextField2.requestFocus();
        }
        
        
        
        else{  
                  try{ 
                      
              Connection con = null;
                Conexiones conecti = new Conexiones();
                con = conecti.getConnection();
                Statement st = con.createStatement();
                
               String sql = "insert into Arreglos(Arreglo,PrecioArreglo) values (?,?)";
               PreparedStatement pst = con.prepareStatement(sql);
                pst.setString(1, jTextField1.getText().trim());
                pst.setString(2, jTextField2.getText().trim());                             
                int n = pst.executeUpdate();
                
                if (n > 0)
                {
                    status.setText("ARREGLO GUARDADO CORRECTAMENTE");
                    
                    VaciarTablaArreglos();
                    VerDatosArreglos();
                    DesabilitarArreglos();
                    limpiar();
                    jButton4.setEnabled(true);
                    jButton9.setEnabled(false);
                }
                }
        catch (SQLException | HeadlessException e)
            {
                JOptionPane.showMessageDialog(null, e);
                JOptionPane.showMessageDialog(this, "EL ARREGLO NO HA SIDO GUARDADO CORRECTAMENTE", "Error", JOptionPane.ERROR_MESSAGE);
            }
        
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

