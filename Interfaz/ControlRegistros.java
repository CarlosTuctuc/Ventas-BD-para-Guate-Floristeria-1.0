    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       
        int opc = JOptionPane.showConfirmDialog(this, "¿ESTA SEGURO QUE DESEA ELIMINAR EL ARREGLO "+jTextField1.getText()+"?", "Pregunta", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (opc == JOptionPane.YES_OPTION)
            {
                try
                {
                    Connection con = null;
                    Conexiones conect = new Conexiones();
                    con = conect.getConnection();
                    Statement st = con.createStatement();
                    String sql = "delete from Arreglos where IdArreglo = ?";
                    PreparedStatement pst = con.prepareStatement(sql);
                    pst.setInt(1, Integer.parseInt(IdArreglo.getText()));
                    int n = pst.executeUpdate();
                    if (n > 0)
                    {
                        JOptionPane.showMessageDialog(this, "DATOS ELIMINADOS CORRECTAMENTE");
                        
                    }
                } catch (SQLException ex)
                {
                    JOptionPane.showMessageDialog(this, "DATOS NO ELIMINADOS CORRECTAMENTE" + ex.getMessage());
                }
                DesabilitarArreglos();
        DesabilitarZonas();
        VaciarTablaArreglos();
        VaciarTablaZonas();
        VerDatosArreglos();
        VerDatosZonas();
        jButton9.setEnabled(false);
        jButton10.setEnabled(false);
        jButton4.setEnabled(true);
        jButton8.setEnabled(true);
            }
        
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

