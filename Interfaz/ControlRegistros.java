    public void VerDatosArreglos(){
        try {
           
            Connection cone = null;
            DefaultTableModel miModelo = (DefaultTableModel) jTable1.getModel();
            Conexiones conect1 = new Conexiones();
            cone = conect1.getConnection();
            String dts[]=new String[3];
            String sql = "select * from Arreglos";
            Statement st = cone.createStatement();
            ResultSet rs = st.executeQuery(sql);
           
            while (rs.next()){
                dts[0] = rs.getString("IdArreglo");
                dts[1] = rs.getString("Arreglo");
                dts[2] = rs.getString("PrecioArreglo");
                
                miModelo.addRow(dts);
                
            } 
//    
            jTable1.setModel(miModelo);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "NO SE PUEDEN VISUALIZAR LOS DATOS DE LA TABLA ARREGLOS", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
